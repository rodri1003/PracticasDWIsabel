// Operadores aritméticos
let suma = 10 + 5; // Suma
let resta = 10 - 5; // Resta
let multiplicacion = 10 * 5; // Multiplicación
let division = 10 / 5; // División
console.log(suma, resta, multiplicacion, division); // Imprime: 15 5 50 2

// Operadores de comparación
let esMayor = 10 > 5; // true
let esIgual = 10 == "10"; // true (comparación débil)
let esEstrictamenteIgual = 10 === "10"; // false (comparación estricta)
console.log(esMayor, esIgual, esEstrictamenteIgual); // Imprime: true true false
